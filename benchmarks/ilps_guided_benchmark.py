#!/usr/bin/env python
"""
Benchmarking script for ILPS-guided transformer model execution strategies.

This script benchmarks transformer model performance using different execution strategies
(balanced partitioning, recomputation, etc.) based on ILP solver solutions. It measures
execution time and peak memory usage for each strategy and reports the results.

Usage:
    torchrun --nproc-per-node=NUM_GPUS benchmarks/ilps_guided_benchmark.py --config_file CONFIG_FILE --solution_file SOLUTION_FILE --output_file OUTPUT_FILE --n N --solution_type SOLUTION_TYPE [options]

Arguments:
    --restart: Start fresh by overwriting any existing output file
    --log: Logging level (info, debug, or none)
    --solution_file: Path to the ILP solution file generated by runall.py
    --output_file: Path to write benchmark results
    --config_file: Path to the ILP config file with model hyperparameters
    --base: Base scheduler type (default: zbh2)
    --n: Specific n value to benchmark
    --solution_type: Specific solution type to benchmark (e.g., "remat", "balance", "combined")

Note: This script must be run in a distributed setting with multiple GPUs.
"""

import os
import sys
import json
import torch
import torch.distributed as dist
import datetime
import argparse
import logging
import psutil
from typing import Dict, List, Optional, Any

sys.path.append(".")
from elf.zb_utils import replace_linear_with_linear_dw
from models.simple import ChainTransformer, FullTransformer  # noqa: F401
from benchmarks.benchmark_utils import bench, get_handcrafted_imbalanced_partition
from benchmarks.zb_schedulers import FullRematScheduler, PartialRematScheduler

logging.basicConfig(level=logging.INFO)


def setup_logging(log_level: str) -> None:
	"""Configure logging level based on command line argument."""
	match log_level:
		case "info":
			logging.getLogger().setLevel(logging.INFO)
		case "debug":
			logging.getLogger().setLevel(logging.DEBUG)
		case "none":
			logging.getLogger().setLevel(100)


def setup_distributed() -> tuple[int, int]:
	"""Initialize distributed training environment."""
	local_rank = int(os.getenv("LOCAL_RANK"))
	torch.cuda.set_device(local_rank)
	dist.init_process_group(backend="nccl", timeout=datetime.timedelta(seconds=300))  # 5 minutes
	return dist.get_rank(), dist.get_world_size()


def load_results(output_file: str, restart: bool) -> Dict:
	"""Load existing results or create new results dictionary."""
	if restart:
		return {}
	try:
		with open(output_file, "r") as f:
			return json.load(f)
	except (FileNotFoundError, json.JSONDecodeError):
		return {}


def load_model_config(config_file: str) -> Dict:
	"""Load model hyperparameters from config file."""
	with open(config_file, "r") as f:
		config = json.load(f)
	return config["model"]


def create_model(model_config: Dict, n: int) -> ChainTransformer:
	"""Create and initialize the model."""
	hidden_size = model_config["hidden_dim"]
	seq_len = model_config["seq_len"]
	num_heads = model_config["n_heads"]
	dropout = model_config["dropout"]

	model = ChainTransformer(hidden_size, n, seq_len, num_heads, dropout)
	replace_linear_with_linear_dw(model, "cpu")
	# for param in model.parameters():
	# 	tensor = param.data.cuda()
	# 	dist.broadcast(tensor, src=0)
	# 	param.data = tensor.cpu()
	return model


def log_model_info(model: ChainTransformer, n: int, rank: int) -> None:
	"""Log model information."""
	if rank == 0:
		print(
			f"\nWith {n} blocks, model has {sum(p.numel() for p in model.parameters()) / 1e9:.2f}B parameters"
		)
		allocated = torch.cuda.memory_allocated() / (1024**3)
		free = torch.cuda.get_device_properties().total_memory / (1024**3) - allocated
		print(f"Memory allocated: {allocated:.2f}GB")
		print(f"Memory free: {free:.2f}GB")


def run_benchmark(
	model: ChainTransformer, parts: Any, scheduler: str, placement: List[int], grad_acc: int = 1, rank: int = 0
) -> tuple[float, List[float]]:
	"""Run benchmark and return iteration time and peak memory usage."""
	iter_time, all_peak_mems = bench(model, parts, scheduler, placement, grad_acc)
	if rank == 0:
		print(f"\t{iter_time:.2f}s, Peak memory: {[f'{m:.2f}' for m in all_peak_mems]} GB")
	return iter_time, all_peak_mems

def get_placement(base: str, world_size: int) -> List[int]:
	if base == "hanayo":
		return [i for i in range(world_size)] + list(reversed([i for i in range(world_size)]))
	elif base == "megatron":
		return [i for i in range(world_size)] * 2
	else:
		return [i for i in range(world_size)]

def process_solution(
	model: ChainTransformer,
	solution: Optional[Dict],
	solution_type: str,
	base: str,
	rank: int,
	world_size: int,
	n: int,
) -> tuple[Optional[float], Optional[List[float]]]:
	"""Process a single solution type and return benchmark results."""
	
	scheduler = solution.pop("scheduler", base)
	placement = get_placement(scheduler, world_size)
	if scheduler == "megatron":
		scheduler = "1f1b"
		
	if solution is None:
		if rank == 0:
			print(f"No {solution_type} solution found for n = {n}")
		return None, None

	if rank == 0:
		print(f"{solution_type.capitalize()}:")

	if solution_type in ["combined", "combinedf"]:
		balance = [int(b) for b in solution["b"]]
		scheduler = PartialRematScheduler(solution, scheduler)

	elif solution_type in ["balance"]:
		balance = [int(b) for b in solution["b"]]

	# FullRemat, Greedy and Baselines are implemented with the FullRematScheduler
	else:
		balance = balanced_partition(n, placement)
		scheduler = FullRematScheduler(solution, scheduler, balance)

	parts = get_handcrafted_imbalanced_partition(model, rank, placement, balance)
	return run_benchmark(model, parts, scheduler, placement, rank=rank)


def balanced_partition(n: int, placement: List[int]) -> List[int]:
	"""Distribute blocks evenly, handling remainder."""
	if n < len(placement):
		logging.getLogger().warning(f"n = {n} is less than the number of GPUs = {len(placement)}")
	blocks_per_gpu = n // len(placement)
	remainder = n % len(placement)
	return [blocks_per_gpu + (1 if i < remainder else 0) for i in range(len(placement))]


def main():
	parser = argparse.ArgumentParser()
	parser.add_argument("--restart", action="store_true", default=False)
	parser.add_argument("--log", choices=["info", "debug", "none"], default="none")
	parser.add_argument(
		"--solution_file",
		type=str,
		default="results/ilps-solutions/default.json",
		help="Path to the solution file",
	)
	parser.add_argument(
		"--output_file",
		type=str,
		default="results/bench-ilps-default.json",
		help="Path to the output file",
	)
	parser.add_argument(
		"--config_file",
		type=str,
		default="ilps/configs/default.json",
		help="Path to the ILP config file with model hyperparameters",
	)
	parser.add_argument("--base", type=str, default="zbh2", help="Base scheduler type")
	parser.add_argument("--n", type=int, required=True, help="Specific n value to benchmark")
	parser.add_argument(
		"--solution_type", type=str, required=True, help="Specific solution type to benchmark"
	)
	args = parser.parse_args()
	setup_logging(args.log)

	rank, world_size = setup_distributed()

	# Ensure output directory exists
	if rank == 0:
		os.makedirs(os.path.dirname(args.output_file), exist_ok=True)

	# Load solutions and results
	with open(args.solution_file, "r") as f:
		ilp_solutions = json.load(f)

	# Load model hyperparameters from config file
	model_config = load_model_config(args.config_file)
	if rank == 0:
		print(f"Using model config from {args.config_file}: {model_config}")

	n = str(args.n)
	if n not in ilp_solutions:
		if rank == 0:
			print(f"No solutions found for n = {n}")
		return

	solutions = ilp_solutions[n]
	if args.solution_type not in solutions:
		if rank == 0:
			print(f"No solution of type {args.solution_type} found for n = {n}")
		return

	# Create and initialize model
	model = create_model(model_config, int(n))
	log_model_info(model, int(n), rank)

	# Process the specific solution type
	model.zero_grad()
	model.cpu()

	if rank == 0:
		gpu_allocated = torch.cuda.memory_allocated() / (1024**3)
		process = psutil.Process(os.getpid())
		cpu_allocated = process.memory_info().rss / (1024**3)
		print(
			f"Memory allocated before {args.solution_type}: GPU {gpu_allocated:.2f}GB, CPU {cpu_allocated:.2f}GB"
		)

	solution = solutions.get(args.solution_type)
	try:
		iter_time, peak_mems = process_solution(
			model, solution, args.solution_type, args.base, rank, world_size, int(n)
		)

		if rank == 0 and iter_time is not None:
			results = load_results(args.output_file, args.restart)
			if n not in results:
				results[n] = {}

			results[n][args.solution_type] = {
				"time": iter_time,
				"peak_mems": [float(m) for m in peak_mems],
			}

			# Save results atomically using a temporary file
			with open(args.output_file, "w") as f:
				json.dump(results, f, indent=4)

	except torch.cuda.OutOfMemoryError:
		print(f"Out of memory for {args.solution_type} with n = {n}")
		dist.destroy_process_group()
		sys.exit(1)

	# Print full stacktrace for any exceptions
	except Exception as e:
		import traceback
		print(f"Error processing {args.solution_type} with n = {n}: {str(e)}")
		print("Full stacktrace:")
		traceback.print_exc()

	if dist.is_initialized():
		dist.destroy_process_group()


if __name__ == "__main__":
	main()
